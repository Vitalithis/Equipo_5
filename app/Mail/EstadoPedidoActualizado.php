    public $pedido;

    public function __construct(Pedido $pedido)
    {
        $this->pedido = $pedido;
    }

    public function build()
    {
        return $this->subject('ActualizaciÃ³n del estado de tu pedido')
                    ->view('emails.estado_pedido');
    }
}
